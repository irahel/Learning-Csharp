//Declarated types
string myName = "Rahel";
int age = 24;
Console.WriteLine(myName);

//String Interpolation
Console.WriteLine($"My name is {myName} and age is {age}");

//Strings is a strong type
Console.WriteLine(myName.Length);

Console.WriteLine(myName.Replace("R", "B"));
Console.WriteLine(myName.ToUpper());
Console.WriteLine(myName.ToLower());
Console.WriteLine(myName.Contains("a"));
Console.WriteLine(myName.Contains("r"));
Console.WriteLine(myName.StartsWith("Ba"));
Console.WriteLine(myName.EndsWith("hel"));
